import ast


def boolify(s):
    if s == 'True' or s == 'true':
        return True
    if s == 'False' or s == 'false':
        return False
    raise ValueError('Not Boolean Value!')


def noneify(s):
    if s == 'None' or s == 'null':
        return None
    raise ValueError('Not None Value!')


def auto_cast(val):
    for caster in (boolify, int, noneify, float, str):
        try:
            return caster(val)
        except ValueError:
            pass
    return val
